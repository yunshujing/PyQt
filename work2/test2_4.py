# 题目4：用元组编程，输入1-7，输出星期几

# 定义星期元组
weekdays = ("星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日")

# 输入并验证
try:
    day = int(input("请输入1-7之间的数字: "))
    
    if 1 <= day <= 7:
        # 使用元组索引（索引从0开始，所以要减1）
        print(f"您输入的是: {weekdays[day - 1]}")
    else:
        print("输入错误！请输入1-7之间的数字。")
        
except ValueError:
    print("输入错误！请输入有效的数字。")

